# Variables: Objects that store data values
x = 10  # integer variable
y = 3.5 # float variable
name = "Shoaib" # string variable
is_student = True # boolean variable

print(x)          # Output: 10
print(y)          # Output: 3.5
print(name)       # Output: Alice
print(is_student) # Output: True

x = x + 5  # Update variable x
print(x)    # Output: 15

#Types of Variables
print(type(x))
print(type(y))
print(type(name))
print(type(is_student))

# --------------- Variable naming rules ----------------

# 1. Variable names must start with a letter or an underscore (_)   
# 2. The rest of the name can contain letters, numbers, or underscores
# 3. Variable names are case-sensitive (e.g., myVar and myvar are different)
# 4. Avoid using Python reserved keywords as variable names 
# 5. Use meaningful variable names for better code readability  
# 6. Use snake_case for variable names (e.g., my_variable_name)
# Examples of valid variable names

fruit_basket = 10
fruit_basket = "Apples, Oranges"
print(fruit_basket)

